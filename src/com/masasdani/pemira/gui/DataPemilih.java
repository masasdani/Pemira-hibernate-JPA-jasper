    private void isiTabel(){    
        DefaultTableModel tabelmodel=new DefaultTableModel(new Object [][] {}, new String [] {"NIM", "Nama", "Jurusan"});
        for(Mahasiswa m : list){
            tabelmodel.addRow(new Object[] {m.getNim(), m.getNama(), m.getJurusan()});
        }
        tabelcari.setModel(tabelmodel);
        sorter=new TableRowSorter(tabelmodel);
        tabelcari.setRowSorter(sorter);
    }

