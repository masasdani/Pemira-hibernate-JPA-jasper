    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(tabelcari.getSelectedRow()!=-1){
            Mahasiswa m=new Mahasiswa();
            m.setNim(tabelcari.getValueAt(tabelcari.getSelectedRow(), 0).toString());
            m.setNama(tabelcari.getValueAt(tabelcari.getSelectedRow(), 1).toString());
            m.setJurusan(tabelcari.getValueAt(tabelcari.getSelectedRow(), 2).toString());
            mahasiswaDao.delete(m);
            btnDelete.setEnabled(false);
            list=mahasiswaDao.getAll();
            isiTabel();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

